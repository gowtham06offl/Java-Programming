public class Assignment_operator {
    public static void main(String[] args) {
        int a = 120;
        System.out.println("The default value of A :"+a);
        a+=10; //a=a+10
        System.out.println(a);
        a-=10;//a=a-10
        System.out.println(a);
        a*=10; //a=a*10
        System.out.println(a);
    }
}
