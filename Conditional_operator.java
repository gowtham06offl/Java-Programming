public class Conditional_operator { // this can be also called as Ternary Operator .
    public static void main(String[] args) {
        int a = 35 , b = 55 , c ; 
        c = a>b?a:b; // the ? means it should satisfy the condition . : means if any conditon satisfy then store that value in declared variable
        System.out.println("The Greatest Number is :"+c);
    }
}
