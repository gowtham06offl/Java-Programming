public class Logical_operator {
    public static void main(String[] args) {
        int mark1= 35 , mark2 = 25;

        System.out.println("And operator && :"+(mark1>=30 && mark2>=30)); // && is a AND operator in which both conditions should satisfy if then output is true 
        System.out.println("or operator || :"+(mark1>=30 || mark2>=30)); // || is a OR operator in which any one of the conditions satisfies then output is true 
    }
}
