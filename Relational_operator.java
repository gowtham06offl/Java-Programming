public class Relational_operator {
    public static void main(String[] args) {
        int a = 50 , b = 20;

        System.out.println("Equal to :"+(a==b)); // == means equal to
        System.out.println("Equal to :"+(a!=b));  // != means not equal to
        System.out.println("Equal to :"+(a>b));  // > means greater than
        System.out.println("Equal to :"+(a<b));  // < means lesser than
        System.out.println("Equal to :"+(a>=b));  // >= means greater than or equal to
        System.out.println("Equal to :"+(a<=b));  // <= means lesser than or equal to
    }
}
